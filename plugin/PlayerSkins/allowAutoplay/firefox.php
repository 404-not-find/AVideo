<p>
    When Firefox is actively blocking media<span class="for" data-for="not fx69"> with sound</span> on a website, you'll see a special icon 
    <img alt="Fx66BlockAutoplayIcon" class="wiki-image frameless" data-original-src="https://user-media-prod-cdn.itsre-sumo.mozilla.net/uploads/gallery/images/2019-03-20-03-14-47-f0bd84.png" src="https://user-media-prod-cdn.itsre-sumo.mozilla.net/uploads/gallery/images/2019-03-20-03-14-47-f0bd84.png" title="">
    in the address bar. 
    <span class="for" data-for="not fx70">
        Click the
        <img alt="Site Info button" class="wiki-image frameless" data-original-src="https://user-media-prod-cdn.itsre-sumo.mozilla.net/uploads/gallery/images/2016-02-25-12-29-33-78136e.png" src="https://user-media-prod-cdn.itsre-sumo.mozilla.net/uploads/gallery/images/2016-02-25-12-29-33-78136e.png" title="">
        in the address bar to open the <a href="/en-US/kb/site-information-panel">Control Center</a>, where you can change the Autoplay
        <span class="for" data-for="not fx69"> sound</span> 
        setting. This option will always appear in the Control Center.
    </span>
    <span class="for" data-for="fx70" style="display: none;">
